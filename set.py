# Coleccion desordenada que no tiene indice

colors = {'Red', 'Green', 'Blue'}

print(type(colors)) # tipo de dato set

print('Red' in colors)
 # ----------------------------

colors.add('Violet')

print(colors)

# ----------------------------

colors.remove('Red')

print(colors)

# Clear

colors.clear()
print(colors)

